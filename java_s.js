function play(){


var audio = new Audio('media/tempo.mp3');
var vol = audio.volume;
var temp = audio.playbackRate;


document.getElementById("start").addEventListener("click", function(){
	if(audio.play()){
		audio.currentTime = 0;
		var v = parseInt(document.getElementById("vd").innerHTML);
		audio.volume = (v/10)*2;

		var t = parseInt(document.getElementById("td").innerHTML);
		audio.volume = t;
		audio.playbackRate = t/10;
	}
	else{
		var v = parseInt(document.getElementById("vd").innerHTML);
		audio.volume = (v/10)*2;

		var t = parseInt(document.getElementById("td").innerHTML);
		audio.playbackRate = t/10;

		audio.play();
	}

});

document.getElementById("stop").addEventListener("click", function(){
	audio.pause();
	audio.currentTime = 0;
	audio.playbackRate = 1;
	audio.volume = 1;
});


document.getElementById("vup").addEventListener("click", function(){
	if(vol<1){
	vol +=0.2;
	audio.volume = vol;
	}
});

document.getElementById("vdown").addEventListener("click", function(){
	if(vol>0.1){
	vol -=0.2;
	audio.volume = vol;
	}
});

document.getElementById("tup").addEventListener("click", function(){
	if(temp<1.2){
	temp +=0.1;
	audio.playbackRate = temp;
	}
});

document.getElementById("tdown").addEventListener("click", function(){
	if(temp>0.8){
	temp -=0.1;
	audio.playbackRate = temp;
	}
});

}

play();